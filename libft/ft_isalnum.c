#include "libft.h"

int	ft_isalnum(int c)
{
	if ((char)c >= '0' && (char)c <= '9')
		return (7);
	if (((char)c >= 'a' && (char)c <= 'z')
		|| ((char)c >= 'A' && (char)c <= 'Z'))
		return (4);
	return (0);
}
