#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*new;
	int		x;
	int		size;

	size = 0;
	while (src[size])
		size++;
	new = malloc(sizeof(char) * (size + 1));
	if (!new)
		return (NULL);
	x = 0;
	while (src[x])
	{
		new[x] = src[x];
		x++;
	}
	new[x] = '\0';
	return (new);
}
